const COMMAND_BY_ID = new Map(DEX_COMMANDS.map((command) => [command.id, command]));

function valuePresent(value) {
  if (typeof value === 'boolean') return value;
  if (value == null) return false;
  return String(value).trim() !== '';
}

function splitPositionalValue(value) {
  const text = String(value ?? '').trim();
  if (!text) return [];
  return text.split(/\s+/).filter(Boolean);
}

export function getDexCommand(commandId) {
  return COMMAND_BY_ID.get(commandId) || null;
}

export function listDexCommands() {
  return DEX_COMMANDS.map((command) => ({ ...command, inputs: [...(command.inputs || [])] }));
}

export function commandNeedsProdConfirmation(command, values = {}) {
  if (!command?.requiresProdConfirmation) return false;
  if (command.danger !== 'PUBLISH') return false;
  const envLike = values.env || values.preflightEnv || values.status || '';
  if (!envLike) return true;
  return String(envLike).toLowerCase() === 'prod';
}

export function validateDexRunRequest(request = {}) {
  const command = getDexCommand(request.commandId);
  if (!command) {
    return { ok: false, error: `Unknown command id: ${request.commandId || '(missing)'}` };
  }
  const values = request.values || {};
  if (commandNeedsProdConfirmation(command, values)) {
    const confirmation = String(request.confirmation || '').trim();
    if (confirmation !== PROD_CONFIRMATION_PHRASE) {
      return {
        ok: false,
        error: `Prod publish requires typed confirmation: ${PROD_CONFIRMATION_PHRASE}`,
        confirmationRequired: true,
      };
    }
  }
  return { ok: true, command };
}

export function buildDexCommandArgs(commandId, values = {}, options = {}) {
  const command = getDexCommand(commandId);
  if (!command) throw new Error(`Unknown command id: ${commandId}`);
  const args = [...command.command];
  for (const input of command.inputs || []) {
    const raw = values[input.name] ?? input.defaultValue;
    if (input.positional) {
      if (valuePresent(raw)) args.push(...splitPositionalValue(raw));
      continue;
    }
    if (!input.flag) continue;
    if (input.type === 'boolean') {
      if (raw === true || raw === 'true' || raw === '1' || raw === 'yes' || raw === 'on') args.push(input.flag);
      continue;
    }
    if (!valuePresent(raw)) continue;
    args.push(input.flag, String(raw).trim());
  }
  if (options.dryRun && command.supportsDryRun && !args.includes('--dry-run')) {
    args.push('--dry-run');
  }
  return args;
}

export function registryPayload() {
  return {
    version: 1,
    confirmationPhrase: PROD_CONFIRMATION_PHRASE,
    groups: DEX_COMMAND_GROUPS,
    commands: listDexCommands(),
  };
}
